public class VariableChangeShortcuts{
	public static void main (String[] args){
		int i,j,k;
		i = 6;
		j= 7;
		k= 8;
		System.out.println( "i: " + i + "\tj: " + j + "\tk: " + k );
       i = i + 3;
      j = j - 3;
      k = k * 3;
	  System.out.println( "i: " + i + "\tj: " + j + "\tk: " + k + "\n" );
}
}